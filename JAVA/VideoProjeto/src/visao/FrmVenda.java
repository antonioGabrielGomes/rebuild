    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        modelo.setNomeProduto(jTextFieldProduto.getText());
        modelo.setQtdItem(Integer.parseInt(jTextFieldQuantidade.getText()));
        modelo.setIdVenda(codVenda);

        int quantidade = controle.getProduto(modelo.getNomeProduto());

        if (quantidade > modelo.getQtdItem()) {
            int result = quantidade - modelo.getQtdItem();

            controle.adicionaItem(modelo, result);
            tabelaItens();

        } else {
            JOptionPane.showMessageDialog(rootPane, "A quantidade desejada n√£o est√° disponivel!");
        }

        SomaProduto();
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

