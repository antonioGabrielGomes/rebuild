    private void jButtonPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrimeiroActionPerformed
        jButtonAlterar.setEnabled(true);
        jButtonDeletar.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jFormattedTextFieldNumero.setEnabled(true);

        try {
            conectaTelefone.executaSQL("select * from telefone order by id_telefone");
            conectaTelefone.rs.first();

            jTextFieldCod.setText(String.valueOf(conectaTelefone.rs.getInt("id_telefone")));
            jFormattedTextFieldNumero.setText(conectaTelefone.rs.getString("numero_tel"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao setar o primeiro registro! \nERRO " + ex);
        }


    }//GEN-LAST:event_jButtonPrimeiroActionPerformed

